class Books {
  final String id;
  final String title;
  final String author;
  final bool isPaid;
  final String content;
  final String synopsis;
  final String imageUrl;

  const Books({
    required this.id,
    required this.title,
    required this.author,
    required this.isPaid,
    required this.content,
    required this.synopsis,
    required this.imageUrl,
  });
}
